//25.已知arr数组内容[1, 3, 5, 8, 10, 11, 16]，编码实现：数组中所有奇数加1，偶数-1,最后打印数组内容

//声明数组
var arr = [1, 3, 5, 8, 10, 11, 16]
//遍历
for (var i = 0; i < arr.length; i++) {
    //如果数组中的元素是否为偶数
    if (arr[i] / 2 == 0) {
        //如果元素为偶数则这些数减一
        arr[i] = (arr[i] - 1)
    } else {//反之元素加一
        arr[i] = (arr[i] + 1)
    }//打印数组
} console.log(arr)