//30.左奇右偶
//10个整数的数组[26,67,49,38,52,66,7,71,56,87],元素重新排列，所有的奇数保存到数组左边，所有的偶数保存到数组右边。

//声明一个数组
var arr = [26,67,49,38,52,66,7,71,56,87]
//声明一个新数组
var arr1 = []
//遍历
for (var i =0; i <arr.length;i++){
    //判断元素是否能%2
    if(arr[i]%2==0){
        //将能够%2的数添加到新数组的末尾
        arr1.push(arr[i])
        //反之
    }else{
        //将不能%2的数添加到新数组的开头
        arr1.unshift(arr[i])
    }
}//打印
console.log(arr1)