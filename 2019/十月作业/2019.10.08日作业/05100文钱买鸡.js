// 5.5文钱可以买一只公鸡，3文钱可以买一只母鸡，1文钱可以买3只雏鸡。现在用100文钱买100只鸡，
// 那么各有公鸡、母鸡、雏鸡多少只？

for (var i = 0; i < 100; i++) {
    if (i % 3 == 0) {
        for (var j = 0; j < (100 - i); j++) {
            if (i * 1 / 3 + j * 5 + (100 - i - j) * 3 == 100) {
                console.log("雏鸡" + i * 1 / 3 + "文买" + i + "只，公鸡"
                    + j * 5 + "文买" + j + "只，母鸡" + (100 - i - j) * 3
                    + "文买" + (100 - i - j) + "只");
            }
        }
    }
}